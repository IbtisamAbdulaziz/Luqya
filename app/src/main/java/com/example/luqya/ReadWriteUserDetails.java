package com.example.luqya;

public class ReadWriteUserDetails {

    public String fullName, doD, userName, userType, phone;

    public ReadWriteUserDetails(String textFullName, String textDoB, String textUserName, String textUserType, String textPhone){

        this.fullName = textFullName;
        this.doD = textDoB;
        this.userName = textUserName;
        this.userType = textUserType;
        this.phone = textPhone;
    }

}
